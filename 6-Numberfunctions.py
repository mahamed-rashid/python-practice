#Basic Number functions. To work with complex numbers, use the math module.
import math
#round function rounds the number to the nearest integer
print(round(3.1))
#abs function returns the absolute value of the number. If the number is negative, it returns the positive value of the number. If the number is positive, it returns the same number.
print(abs(-3.1))
#ceil function returns the smallest integer greater than or equal to the number
print(math.ceil(3.1))
#floor function returns the largest integer less than or equal to the number
print(math.floor(3.1))
#sqrt function returns the square root of the number. isqrt function returns the integer part of the square root of the number.
print(math.sqrt(100))