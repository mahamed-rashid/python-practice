course = "Python for Beginners"
#len function returns the number of characters in a string
print(len(course))
#strings are zero-based index in python
print(course[0])
#negative index
print(course[-1])
#slicing it returns a new string with the characters in the specified range. starts from index 0 and ends at 2 (3-1)
print(course[0:3])
#the below code prints the entire string
print(course[0:])
#the below code prints the pyt. by default, the start index is 0
print(course[:3])
#the below code prints the entire string
print(course[:])